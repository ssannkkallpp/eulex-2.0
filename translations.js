// Language management class
class LanguageManager {
    constructor() {
        this.currentLanguage = 'en';
        this.loadLanguage();
    }
    
    loadLanguage() {
        const savedLanguage = localStorage.getItem('eulex-language');
        if (savedLanguage && translations[savedLanguage]) {
            this.currentLanguage = savedLanguage;
        }
    }
    
    setLanguage(languageCode) {
        if (translations[languageCode]) {
            this.currentLanguage = languageCode;
            localStorage.setItem('eulex-language', languageCode);
            this.updateUI();
        }
    }
    
    getText(key) {
        const lang = translations[this.currentLanguage];
        return lang && lang[key] ? lang[key] : translations.en[key] || key;
    }
    
    updateUI() {
        // Update all elements with data-i18n attributes
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            const text = this.getText(key);
            if (element.tagName === 'INPUT' && element.type === 'placeholder') {
                element.placeholder = text;
            } else {
                element.textContent = text;
            }
        });
        
        // Update language selectors
        const languageSelects = document.querySelectorAll('#languageSelect, #settingsLanguageSelect');
        languageSelects.forEach(select => {
            if (select.value !== this.currentLanguage) {
                select.value = this.currentLanguage;
            }
        });
    }
    
    getAvailableLanguages() {
        return Object.keys(translations).map(code => ({
            code,
            name: this.getLanguageName(code)
        }));
    }
    
    getLanguageName(code) {
        const names = {
            en: 'English',
            es: 'Español',
            fr: 'Français',
            de: 'Deutsch',
            it: 'Italiano',
            pt: 'Português',
            ru: 'Русский',
            zh: '中文',
            ja: '日本語',
            ko: '한국어',
            ar: 'العربية',
            hi: 'हिन्दी'
        };
        return names[code] || code;
    }
}

// Initialize language manager globally
window.languageManager = new LanguageManager(); 